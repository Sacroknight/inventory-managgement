    public enum TipoConflicto {
    NINGUNO,
    USUARIO_EXISTENTE,
    CORREO_EXISTENTE,
    AMBOS_EXISTENTES
    }

    public TipoConflicto consultaUsuario(String u, String p, String e) {
        int_usuarioDaoImp intUsuariosDAO;
        intUsuariosDAO = new int_usuarioDaoImp(conn);
        int_usuario usuario;

        // Verificar si el usuario ya existe
        usuario = intUsuariosDAO.getUserByNameAndPass(u, p);
        if (usuario != null) {
            // Usuario ya existe
            return TipoConflicto.USUARIO_EXISTENTE;
        }

        // Verificar si el correo ya existe
        usuario = intUsuariosDAO.getUserByEmailAndPass(e, p);
        if (usuario != null) {
            // Correo ya existe
            return TipoConflicto.CORREO_EXISTENTE;
        }

        // Si llegamos a este punto, ni el usuario ni el correo existen
        return TipoConflicto.NINGUNO;
    }

