    private void btn_registroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_registroMouseClicked

        conexionDB conexion = new conexionDB();
        conn = conexion.establecerConexion();

        try {
            String username = txt_user.getText();
            String name = txt_nombre.getText();
            String apellido = txt_apellido.getText();
            String email = txt_correo.getText();
            String direc = txt_direccion.getText();
            String password = new String(txt_pass.getPassword());

            // Validar campos vacíos o con texto por defecto
            if (username.isEmpty() || username.equals("Nombre de usuario") ||
                name.isEmpty() || name.equals("Digite su Nombre") ||
                apellido.isEmpty() || apellido.equals("Digite su Apellido") ||
                password.isEmpty() || password.equals("********")) {
                // Mostrar mensaje de llenar todos los campos
                JOptionPane.showMessageDialog(null, "Por favor, llene todos los campos.", "Error", JOptionPane.ERROR_MESSAGE);
            } else if (email.isEmpty() || email.equals("Digite un email") || !isValidEmail(email)) {
                // Mostrar mensaje de correo electrónico inválido
                JOptionPane.showMessageDialog(null, "Por favor, ingrese un correo electrónico válido.", "Error", JOptionPane.ERROR_MESSAGE);
            }  else {
                // Campos llenos, imprimir información (puedes comentar esto en producción)
                //                System.out.println("Username: " + username);
                //                System.out.println("Nombre: " + name);
                //                System.out.println("Apellido: " + apellido);
                //                System.out.println("Correo: " + email);
                //                System.out.println("Dirección: " + direc);
                //                System.out.println("Contraseña: " + password);
                camposllenos = true;
            }

            // Verificar aceptación de términos y condiciones
            if (aceptoTerminos) {
                aceptoTerminos_2 = true;
            } else {
                // Muestra un mensaje de error al usuario
                JOptionPane.showMessageDialog(this, "Acepte los términos y condiciones");
            }

            // Si se aceptan términos, campos llenos y el usuario es nuevo, proceder con el registro
            if (aceptoTerminos_2 && camposllenos) {
                // La función consultaUsuario debería devolver un TipoConflicto indicando el tipo de conflicto
                TipoConflicto tipoConflicto = consultaUsuario(username, password, email);

                switch (tipoConflicto) {
                    case NINGUNO:
                    // Ni el usuario ni el correo existen, puedes proceder con tu lógica
                    System.out.println("Usuario y correo no registrados. Puedes continuar con tu lógica.");

                    // Registrar el nuevo cliente en la base de datos
                    String descrip = "Cliente";
                    insertarcliente(username, name, apellido, email, direc, password, descrip);

                    JOptionPane.showMessageDialog(this, "Registro exitoso. A continuación, debe iniciar sesión.");

                    // Iniciar sesión y redirigir al usuario a la pantalla de login
                    login a = new login();
                    a.setVisible(true);
                    dispose();
                    break;
                    case USUARIO_EXISTENTE:
                    // Usuario ya existe, muestra un mensaje
                    JOptionPane.showMessageDialog(this, "El usuario ya está registrado.");
                    break;
                    case CORREO_EXISTENTE:
                    // Correo ya existe, muestra un mensaje
                    JOptionPane.showMessageDialog(this, "El correo ya está registrado.");
                    break;
                    case AMBOS_EXISTENTES:
                    // Ambos usuario y correo existen, maneja según tus necesidades
                    // Puedes agregar aquí la lógica específica para el caso de ambos existentes
                    JOptionPane.showMessageDialog(this, "El usuario y el correo ya están registrados.");
                    break;
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
            // Manejar la excepción de manera apropiada
            JOptionPane.showMessageDialog(this, "Error durante el registro.", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btn_registroMouseClicked

